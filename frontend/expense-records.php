        url += '/' + editingExpenseId;
        method = 'PUT';
    }

    $.ajax({
        url: url,
        method: method,
        contentType: 'application/json',
        data: JSON.stringify(data),
        success: function(response) {
            if (response.success) {
                showMessage(editingExpenseId ? 'ÏßÄÏ∂úÏù¥ ÏàòÏ†ïÎêòÏóàÏäµÎãàÎã§.' : 'ÏßÄÏ∂úÏù¥ Í∏∞Î°ùÎêòÏóàÏäµÎãàÎã§.', 'success');
