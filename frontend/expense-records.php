function updatePagination(pagination) {
    if (!pagination || pagination.pages <= 1) {
        $('#pagination').empty();
        return;
    }

    let paginationHtml = '';

    // Ïù¥Ï†Ñ ÌéòÏù¥ÏßÄ
    if (pagination.page > 1) {
        paginationHtml += '<button class="btn waves-effect waves-light" onclick="changePage(' + (pagination.page - 1) + ')"><i class="material-icons left">chevron_left</i>Ïù¥Ï†Ñ</button>';
    }

    // ÌéòÏù¥ÏßÄ Î≤àÌò∏Îì§
    const startPage = Math.max(1, pagination.page - 2);
    const endPage = Math.min(pagination.pages, pagination.page + 2);

    for (let i = startPage; i <= endPage; i++) {
        if (i === pagination.page) {
            paginationHtml += '<button class="btn blue disabled">' + i + '</button>';
        } else {
            paginationHtml += '<button class="btn waves-effect waves-light" onclick="changePage(' + i + ')">' + i + '</button>';
        }
    }

    // Îã§Ïùå ÌéòÏù¥ÏßÄ
    if (pagination.page < pagination.pages) {
        paginationHtml += '<button class="btn waves-effect waves-light" onclick="changePage(' + (pagination.page + 1) + ')">Îã§Ïùå<i class="material-icons right">chevron_right</i></button>';
    }

    $('#pagination').html(paginationHtml);
}

